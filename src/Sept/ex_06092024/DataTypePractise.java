package Sept.ex_06092024;

public class DataTypePractise {
    public static void main(String[] args) {
        // Range of byte is -128 to 127
        byte num_byte = 127;
        System.out.println(num_byte);
        // Range of byte is -32,768 to 32,767
        short num_short = 3276;
        System.out.println(num_short);
        char c = 'C';
        System.out.println(c);
        boolean isGood = true;
        System.out.println(isGood);
    }
}
