package Sept.ex_06092024;

public class SwapTwoNumbers {
    public static void main(String[] args) {
        int a = 5;
        int b = 6;
        int temp = 0;
        temp = a;
        a = b;
        b = temp;
        System.out.println("Value of a now is "+ a);
        System.out.println("Value of b now is "+ b);
    }
}
